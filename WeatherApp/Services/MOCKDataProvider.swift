import UIKit

struct CityWeatherData: Hashable {
    let titleData: TitleData
    let dayHourlyDescription: String
    let dayHourlyData: [DayHourlyData]
    let dayData: [DayData]
}

struct TitleData: Hashable {
    let title: String
    let subtitle: String?
    let currentTemp: Int
    let description: String
    let minTemp: Int
    let maxTemp: Int
}

struct DayHourlyData: Hashable {
    let hour: String
    let imageSystemName: String
    let temp: Int

    var id: String {
        "\(hour) \(imageSystemName) \(temp)"
    }
}

struct DayData: Hashable {
    let title: String
    let imageSystemName: String
    let minTemp: Double
    let maxTemp: Double
    let minDayTemp: Double
    let maxDayTemp: Double
    let currentTemt: Double?

    var id: String {
        "\(title) \(imageSystemName) \(minTemp) \(maxTemp) \(minDayTemp) \(maxDayTemp) \(String(describing: currentTemt))"
    }
}

